class Persona:
    
    def __init__(self,cedula,celular,nombre, sexo, edad):
        self.cedula = cedula
        self.celular = celular
        self.nombre = nombre
        self.sexo = sexo
        self.edad = edad
        
        
    def getCedula(self):
        return self.cedula
    
    
    def setCedula(self,cedula):
        self.cedula=cedula
        
    
    def getCelular(self):
        return self.celular
         
    def setCelular(self,celular):
        self.celular=celular
        
        
        
    def getNombre(self):
        return self.nombre
    
    def setNombre(self,nombre):
        self.nombre=nombre
        
        
        
    def getSexo(self):
        return self.sexo
    
    def setSexo(self,sexo):
        self.sexo=sexo
        
        
        
    def getEdad(self):
        return self.edad
    
    def setEdad(self,edad):
        self.edad=edad
        
    
        
        
        
        
        
